package StringExample;

public class RplaceMethod {

    public static void main(String[] args) {

        String car = "honda";
        System.out.println(car.replace("h", "H"));

        car = car.replace("honda", "toyota");  //toyota
        System.out.println(car);

        System.out.println(car.replace("o", "1"));  //t1y1ta








    }
}
